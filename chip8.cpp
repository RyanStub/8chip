Chip8::Chip8()
{
    initialize();
}




void Chip8::initialize()
{
    //
    pc = 0x200; 
    opcode = 0;
    I = 0;
    sPtr = 0;

    for(int i = 0; i < sizeof(stack)/sizeof(*stack); i++)
        stack[i] = 0;

    for(int i = 0; i < sizeof(memory)/sizeof(*memory); i++)
        memory[i] = 0;

    for(int i = 0; i < sizeof(V)/sizeof(*V); i++)
        V[i] = 0; 

    for(int i = 0; i < sizeof(screen)/sizeof(*screen); i++)
        screen[i] = 0; 

    for(int i = 0; i < 80; ++i)
        memory[i] = fontset[i];
}

